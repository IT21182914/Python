# Creating an array (list) of numbers
numbers = [1, 2, 3, 4, 5]

# Accessing elements in the array
print(numbers[0])  # Prints the first element (1)
print(numbers[2])  # Prints the third element (3)

# Modifying elements in the array
numbers[1] = 10    # Changes the second element to 10

# Adding elements to the array
numbers.append(6)  # Appends 6 to the end of the array

# Removing elements from the array
numbers.pop(3)     # Removes the fourth element

# Displaying the modified array
print(numbers)     # Prints the modified array
